    private void btnSigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSigninActionPerformed
        String user_type = jComboBox1.getSelectedItem().toString();
        String username = txtUsername.getText();
        String password = txtPassword.getText();

        if (user_type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a user type.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (username.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your username", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter your password", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `user` "
                        + "INNER JOIN `user_type` ON `user`.`user_type_id` = `user_type`.`id` "
                        + "INNER JOIN `employee` ON `user`.`employee_id` = `employee`.`id` "
                        + "WHERE `user_type`.`name` = '" + user_type + "' AND `user`.`username` = '" + username + "' AND "
                                + "`user`.`password` = '" + password + "'");
                if (rs.next()) {
                    if (rs.getString("user.status_id").equals("2")) {
                        JOptionPane.showMessageDialog(this, "User is inactive.", "Warning", JOptionPane.WARNING_MESSAGE);
                        jComboBox1.setSelectedIndex(0);
                        txtUsername.setText("");
                        txtPassword.setText("");
                    } else {
                        EmployeeId = rs.getString("employee.id");
                        UserId = rs.getString("user.id");
                        UserType = rs.getString("user_type.name");

                        ResultSet rs2 = MySQL.search("SELECT * FROM `logger` WHERE `user_id` = '" + UserId + "'");
                        if (rs2.next()) {
                            if (rs2.getString("logout").equals("0")) {
                                JOptionPane.showMessageDialog(this, "User already has logged in.", "Warning", JOptionPane.WARNING_MESSAGE);
                            } else {
                                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                                dNow = sdf.format(new Date());
                                MySQL.push("INSERT INTO `logger` (`login`,`datetime_in`,`user_id`) VALUES ("
                                        + "'1','" + dNow + "','" + UserId + "')");

                                if (UserType.equals("Admin")) {
                                    HomeA homeA = new HomeA();
                                    homeA.setVisible(true);
                                } else {
                                    HomeC homeC = new HomeC();
                                    homeC.setVisible(true);
                                }
                                this.dispose();
                            }

                        } else {
                            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                            dNow = sdf.format(new Date());
                            MySQL.push("INSERT INTO `logger` (`login`,`datetime_in`,`user_id`) VALUES ("
                                    + "'1','" + dNow + "','" + UserId + "')");

                            if (UserType.equals("Admin")) {
                                HomeA homeA = new HomeA();
                                homeA.setVisible(true);
                            } else {
                                HomeC homeC = new HomeC();
                                homeC.setVisible(true);
                            }
                            this.dispose();
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid username or password.", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSigninActionPerformed

